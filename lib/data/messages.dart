String getRandomMessage(MessageCategory category) {
  final list = messages[category]!;
  return list[Random().nextInt(list.length)];
}

String getPostponeMessage(int postponedCount) {
  if (postponedCount >= 3) {
    return getRandomMessage(MessageCategory.brutalPostponeMax);
  }
  return getRandomMessage(MessageCategory.brutalPostpone);
}
